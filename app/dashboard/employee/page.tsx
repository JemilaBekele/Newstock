import PageContainer from '@/components/layout/page-container';
import { buttonVariants } from '@/components/ui/button';
import { Heading } from '@/components/ui/heading';
import { Separator } from '@/components/ui/separator';
import { DataTableSkeleton } from '@/components/ui/table/data-table-skeleton';
import EmployeeListingPage from '@/features/Employee/components/employee-listing';
import UserTableAction from '@/features/Employee/components/employee-table-action';
import { searchParamsCache } from '@/lib/searchparams';
import { cn } from '@/lib/utils';
import { IconPlus } from '@tabler/icons-react';
import Link from 'next/link';
import { SearchParams } from 'nuqs/server';
import { Suspense } from 'react';
import { PermissionGuard } from '@/components/PermissionGuard';
import { PERMISSIONS } from '@/stores/permissions';
export const metadata = {
  title: 'Dashboard: Employee'
};

type pageProps = {
  searchParams: Promise<SearchParams>;
};

export default async function EmployeePage(props: pageProps) {
  const searchParams = await props.searchParams;
  // Allow nested RSCs to access the search params (in a type-safe way)
  searchParamsCache.parse(searchParams);

  // This key is used for invoke suspense if any of the search params changed (used for filters).
  // const key = serialize({ ...searchParams });

  return (
    <PageContainer scrollable={false}>
      <div className='flex flex-1 flex-col space-y-4'>
        <div className='flex items-start justify-between'>
          <Heading title='Employee' description='Manage Employee ' />
          <PermissionGuard requiredPermission={PERMISSIONS.Employee.VIEW.name}>
            <Link
              href='/dashboard/employee/new'
              className={cn(buttonVariants(), 'text-xs md:text-sm')}
            >
              <IconPlus className='mr-2 h-4 w-4' /> Add New
            </Link>{' '}
          </PermissionGuard>
           <PermissionGuard requiredPermission={PERMISSIONS.REPORT.RESET.name}>
            <Link
              href='/dashboard/Reset'
              className={cn(buttonVariants(), 'text-xs md:text-sm')}
            >
              <IconPlus className='mr-2 h-4 w-4' /> Re
            </Link>{' '}
          </PermissionGuard>
        </div>
        <Separator />
        <UserTableAction />
        <Suspense
          // key={key}
          fallback={
            <DataTableSkeleton columnCount={5} rowCount={8} filterCount={2} />
          }
        >
          <EmployeeListingPage />
        </Suspense>
      </div>
    </PageContainer>
  );
}
